public class City {
    private String cityId;
    private String city;
    private String countryId;
    private String lastUpdate;

    public City(String cityId, String city, String countryId, String lastUpdate) {
        this.cityId = cityId;
        this.city = city;
        this.countryId = countryId;
        this.lastUpdate = lastUpdate;
    }
}
